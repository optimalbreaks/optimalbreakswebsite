<h2>Por qué encaja en Optimal Breaks</h2>
<p><em>Rave Culture. A new era</em> no es un documental solo de «breakbeat» en sentido estricto, pero la cultura que retrata —fiestas ilegales, radio pirata, baterías aceleradas y energía de graves en el Reino Unido de los noventa— es el mismo caldo del que salió el <strong>breakbeat hardcore</strong> y buena parte de lo que vino después. Cuando el relato salta de Inglaterra a <strong>Tenerife</strong>, también cuenta cómo esas ideas <strong>viajaron</strong>: de almacenes a islas, de mito a archivo.</p>

<h2>De qué va el filme</h2>
<p>Dirigido por <strong>Eduardo Cubillo</strong> y producido por <strong>Jonay Amador (DJ Jonay)</strong> junto a <strong>La Gaveta Producciones</strong>, el proyecto se presenta como un documental sociohistórico con los ritmos sincopados en el centro: entrevistas, archivo y una banda sonora pensada tanto para la pista como para la sala. El equipo enmarca los inicios del rave británico como una contracultura social y política moldeada, en parte, por el clima de los años de Thatcher — juventud, urgencia y escape colectivo.</p>
